#tuples are same than lists but immutable
# ❌ Pas possible de modifier un élément existant (t[0] = x).

# ❌ Pas possible de supprimer un élément spécifique (del t[1]).

# ❌ Pas possible d’ajouter un élément directement (t.append(x)).

# ❌ Pas possible de réorganiser les éléments directement (t.sort() ou t.reverse()).

# ✅ Possible de recréer un nouveau tuple avec les changements (t = t + (x,)).

# ✅ Possible de lire les éléments par index (t[0]).

# ✅ Possible de parcourir un tuple avec une boucle (for x in t:).

# ✅ Possible de vérifier la présence d’un élément (x in t).

# ✅ Possible de créer un tuple vide (t = ()) ou à un seul élément (t = (x,)).

# ✅ Possible de supprimer le tuple entier (del t).

# ✅ Possible d’utiliser un tuple comme clé dans un dictionnaire (d[(1,2)] = "valeur").

# ✅ Possible de contenir des types variés (nombres, chaînes, listes, etc.).

# ✅ Possible de stocker un tuple dans un autre tuple (tuple imbriqué).